        // TODO add your handling code here:
        try
        {
        String a = jTextField1.getText();
        int S_Id = Integer.parseInt(a);
        try
        {   
            Class.forName("java.sql.DriverManager");
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/institute","root","sql");
            Statement stmt = (Statement) con.createStatement();
            String query ="DELETE FROM StudentDetails WHERE S_Id="+S_Id+";";
            stmt.executeUpdate(query);
            String q ="DELETE FROM Batch WHERE S_Id="+S_Id+";";
            stmt.executeUpdate(q);
            JOptionPane.showMessageDialog(this, "Information deleted");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Please give the correct Student Id");
        }
